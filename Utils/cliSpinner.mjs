
const Frames=["*","**","***","****","*****","******","*******","********","*********","**********","***********","************","*************","**************","***************","****************","*****************","******************","*******************","********************","*********************","**********************","***********************","************************","*************************","**************************","***************************","****************************","*****************************","******************************","*******************************","********************************","*********************************","**********************************","***********************************","************************************","*************************************","**************************************","***************************************","****************************************","*****************************************","******************************************","*******************************************","********************************************","*********************************************","**********************************************","***********************************************","************************************************","*************************************************"];
let i=0

const Spinner=setInterval(()=>{
    process.stdout.write(`\r${Frames[i++ % Frames.length]}`);
},100);

const startSpinner=()=>{
    i=0;
    Spinner;
}

const stopSpinner=()=>{
    clearInterval(Spinner);
    process.stdout.write('\r\x1b[K'); // Clear the line
}

export {startSpinner, stopSpinner};